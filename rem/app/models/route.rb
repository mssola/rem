class Route < ActiveRecord::Base
  attr_accessible :name, :user_id, :desc, :protected, :rating

  validates_presence_of :name, :on => :create
  validates_uniqueness_of :name, :scope => :user_id
  validates_length_of :desc, :maximum => 160

  belongs_to :user
end
